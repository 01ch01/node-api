const mongoose = require('mongoose');

// creating model with mongoose 'language' (sql -> json)
const ProductSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  url: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

// register model
mongoose.model('Product', ProductSchema);
